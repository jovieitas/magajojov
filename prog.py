# Definindo as variáveis
num1 = 10
num2 = 20
num3 = 30
num4 = 40

frase = "Python é uma linguagem de programação poderosa."
palavra = "linguagem"

# Calculando a média aritmética
media = (num1 + num2 + num3 + num4) / 4

# Calculando o quadrado de um dos números
quadrado_num1 = num1 ** 2

# Calculando o dobro de um dos números
dobro_num2 = num2 * 2

# Calculando a quantidade de letras da palavra
qtd_letras_palavra = len(palavra)

# Calculando a quantidade de espaços em branco da frase
qtd_espacos_frase = frase.count(' ')

# Verificando se o primeiro número é maior que o segundo
primeiro_maior_que_segundo = num1 > num2

# Calculando o maior número
maior_numero = max(num1, num2, num3, num4)

# Imprimindo os resultados
print(f"Média aritmética: {media}")
print(f"Quadrado de {num1}: {quadrado_num1}")
print(f"Dobro de {num2}: {dobro_num2}")
print(f"Quantidade de letras da palavra '{palavra}': {qtd_letras_palavra}")
print(f"Quantidade de espaços em branco da frase: {qtd_espacos_frase}")
print(f"O primeiro número é maior que o segundo: {primeiro_maior_que_segundo}")
print(f"Maior número: {maior_numero}")



